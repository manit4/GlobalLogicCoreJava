package com.global;

public class emp {
	int salary;
	int experiene;
	
	
	public emp(int salary, int experiene) {
		super();
		this.salary = salary;
		this.experiene = experiene;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getExperiene() {
		return experiene;
	}
	public void setExperiene(int experiene) {
		this.experiene = experiene;
	}
	
}
